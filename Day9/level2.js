function categorizeCountries(){
  return countries.filter((country)=> {
    country=country.toLowerCase();
    if(country.includes("island")||country.includes("land")||country.includes("ia")||country.includes("stan")){
      return true;
    }else{
      return false;
    }
  });
}
console.log(categorizeCountries())

function nameOfCountry(){
  let countryName = {};
  countries.forEach(country => {
    if(countryName[country[0]]){
      countryName[country[0]] +=1;
    }else{
      countryName[country[0]] = 1;
    }
  });;
  return countryName;
}
console.log(nameOfCountry());

